import UIKit


func add (num1:Double, num2:Double) -> Double {
    return num1 + num2
}
func substract (num1:Int, num2:Int) -> Int {
    return num1 - num2
}
func multiply (num1:Float, num2:Float) -> Float {
    return num1 * num2
}
func divide (num1:Double, num2:Double) -> Double {
    return num1 / num2
}

